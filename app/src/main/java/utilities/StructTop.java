package utilities;

public class StructTop {

    public long   ID;
    public String username;
    public int    type;
    public int    nickName;
    public int    pic;
    public int    score;


    public StructTop(long id, String Username, int Type, int Nickname, int Pic, int Score) {
        ID = id;
        username = Username;
        type = Type;
        nickName = Nickname;
        pic = Pic;
        score = Score;
    }

}
